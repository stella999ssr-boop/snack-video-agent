ALL_FORBIDDEN_CATEGORIES = {
    "absolute_terms": ABSOLUTE_TERMS,
    "medical_terms": MEDICAL_TERMS,
    "snack_sensitive": SNACK_SENSITIVE,
    "child_inducement": CHILD_INDUCEMENT,
    "price_fraud": PRICE_FRAUD,
}


def get_all_forbidden_words() -> list[str]:
    """获取所有禁词的扁平列表"""
    words = []
    for cat in ALL_FORBIDDEN_CATEGORIES.values():
        words.extend(cat.get("top", []))
    return words


def get_alternatives(word: str) -> str:
    """查找某个禁词的替代表达"""
    for cat in ALL_FORBIDDEN_CATEGORIES.values():
        if word in cat.get("alternatives", {}):
            return cat["alternatives"][word]
    return ""


def check_text(text: str) -> list[dict]:
    """
    扫描文本，返回命中的所有违规项。
    返回: [{"category": "absolute_terms", "word": "最好", "suggestion": "..."}, ...]
    """
    hits = []
    for cat_name, cat_data in ALL_FORBIDDEN_CATEGORIES.items():
        for word in cat_data.get("top", []):
            if word in text:
                hits.append({
                    "category": cat_name,
                    "word": word,
                    "suggestion": cat_data.get("alternatives", {}).get(word, ""),
                })
    return hits


